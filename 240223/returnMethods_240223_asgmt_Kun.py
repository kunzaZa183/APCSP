# return methods

def cubeIt( num ) :
    return num * num * num

def guess( val ) :
    return val * 3 + val * 2

print( cubeIt( 4 ) )
print( cubeIt( 3 ) )
print( cubeIt( 5.2 ) )

print( guess( 5 ) )
print( guess( 4 ) )
print( guess( 8 ) )
print( guess( 10.3 ) )

print( guess( 2 ) )
